
# Função de soma
def somar(a, b):
    return float(a) + float(b)


# Função de subtração
def subtrair(a, b):
    return float(a) - float(b)


# Função de divisão
def dividir(a, b):
    if b == 0:
        return 0
    return float(a) / float(b)


#função de multiplicar
def multiplicar(a, b):
    return float(a) * float(b)