# exemplo 1
# a variável contadora deverá assumir valores no intervalo entre 0 e 9
# “para a variável x no intervalo entre 0 e 9, faça:”
for x in range(10):
  #para cada um desses valores, vamos printar o valor da variável
  print(x)

print("---")

